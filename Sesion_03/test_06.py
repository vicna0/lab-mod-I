"""Listas (append): Ordenar los elementos de una lista"""

var_1 = ["Python", "Java", "PHP", "Javascript", "Typescript"]

var_1.append("Python")
var_1.append("Python")
var_1.append("Python")
var_1.append("Javascript")

print("Mi lista actualizada tiene los siguientes valores: {}".format(var_1))

print("La cantidad de veces que se repite 'Python' es: {}".format(var_1.count('Python')))
print("La cantidad de veces que se repite 'Java' es: {}".format(var_1.count('Java')))
