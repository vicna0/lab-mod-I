"""Listas de Python"""

"""Listas (clear): limpia los elementos de la lista"""

var_1= [60.8, 20.9, 120, False, "Av. Ricardo Palma"]

print("Los valores de mi lista var_1 son: {}".format(var_1))

var_1.clear()
print("Mi lista actualizada es: {}".format(var_1))

var_1.append("Peru")
print("Mi lista actualizada es: {}".format(var_1))