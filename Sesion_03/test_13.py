"""Diccionario en Python"""

"""Los nommbres de los key(llaves) van a ir escritos siempre en minusculas (Por convencion)"""

var_1 = {"nombre": "Matias", "edad": 26, "promedio":14}

print("Nuestro diccionario tiene el siguiente contenido: {}".format(var_1))

"""Agregegamos elementos en nuevo key a mi diccionario"""

