"""Listas en Python"""

"""Listas pop(): Quita un elemmento de una posiscion dada"""

paises=["Peru", "Canada", "Argentina", "Mexico"]

print("Mi lista inicial es la siguiente:{}". format(paises))

paises.pop()

print("Mi lista actualizada es la siguiente; {}".format(paises))

paises.pop(1)

print("Mi lista actualizada es la siguiente; {}".format(paises))